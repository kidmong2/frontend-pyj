// 이름 입력받기
let userName = prompt('당신의이름');

// 나이 입력받기
let userAge = prompt('당신의나이');

// 이름과 나이 출력(알림창)
alert(userName + '당신은' + userAge + '살 입니다.');

// 취미 입력받기
let userHobby = prompt('당신의취미');

// 콘솔에 취미 출력
console.log(userName +'취미는' + userHobby + '입니다.');

// 웹 페이지에 이름 출력
document.write('<h1>'+ userName + '</h1>');